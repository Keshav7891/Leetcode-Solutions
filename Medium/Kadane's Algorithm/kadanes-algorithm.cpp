class Solution{
    public:
    // arr: input array
    // n: size of array
    //Function to find the sum of contiguous subarray with maximum sum.
    long long maxSubarraySum(int arr[], int n){
        
        long long res = INT_MIN;
        long long currRes = 0;
        
        for(int i=0;i<n;i++){
            if(currRes < 0){
                currRes = arr[i];
            }else{
                currRes = currRes + arr[i];
            }
            res = max(res,currRes);
        }
        
        return res;
        
    }
};

